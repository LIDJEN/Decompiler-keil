def input_file(file_name):
    file=file_name
    out=[]
    with open(file) as file:
        for line in file:
            if line[-1] == "\n":
                line = line[:-1]
            out.append(line)
        return out

def asm_2_bin(instructions: list[str], console_out=1) -> str:
    """
    переводит из asm в bin возвращает строку
    :param instructions:
    :param console_out - вывод в консоль 0 не выводится 1 выводится:
    :return: 4BF000
    """
    out = ''
    for i in instructions:
        out = out + asm_bin.get(i, i)
    if console_out == 1:
        print("bin out:",out)
    return out

def bin_2_asm(instructions, console_out=1):
    """
    переводит из bin в asm возвращает строку
    :param instructions:
    :param console_out - вывод в консоль 0 не выводится 1 выводится:
    :return:
    """
    instructions = str(instructions[0])
    instructions = instructions.replace(" ", "")
    instructions = instructions.replace("\n", "")
    sort = []

    for i in range(0, len(instructions), 2):
        sort.append(instructions[i:i + 2].upper())

    out = ''
    i = 0
    while i < len(sort):
        for j in range(int(bytes.get(sort[i])[-1])):
            out = out + sort[i+j]
            if j == 0:
                out = out + " "

        out = out + " --> "

        for j in range(int(bytes.get(sort[i])[-1])):
            if j == 0:
                out = out + str(bytes.get(sort[i-j]))[:-2]
            else:
                out = out.replace("{}", sort[i], 1)
            i += 1

        out = out + "\n"

    if console_out == 1:
        print(out)
    return out

def get_key(d, value):
    """
    возвращает ключ искомого значения
    :param d - словарь:
    :param value - значение ключ которого ищется:
    :return:
    """
    if value == "??":
        return "Invalid instruction"
    for k, v in d.items():
        if v == value:
            return k

